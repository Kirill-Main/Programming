#кратность 51
n = int(input("Введите количество чисел: "))
k3 = k17 = k51 = 0

for i in range(0,n,1):
    a = int(input("Введите число a: "))
    if a % 51 == 0:
        k51 += 1
    elif a % 17 == 0:
        k17 += 1
    elif a % 3 == 0:
        k3 += 1
k=(k51*(k51-1))%2 + k51*(n-k51) + k3*k17
print(f"Количество пар, произведение которых кратно 51 равно{k}")
